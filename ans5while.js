// Q5. Use the while loop to print numbers from 1 to 25 in ascending and descending order.

// numbers from 1 to 25 in ascending order.

let number =1

while (number<=25) {
    console.log(number);
    number=number+1;
}

//  numbers from 1 to 25 in descending order.

let number1 =25

while (number1>=1) {
    console.log(number1);
    number1=number1-1;
}

